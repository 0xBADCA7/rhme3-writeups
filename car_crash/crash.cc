unsigned char sub_129(unsigned char a, unsigned char b) {
	// entry: 0xae, 0x94
	unsigned char acc=0;  // Y+1
	//char a;  // Y+2
	//char b;  // Y+3
	for ( ; b!=0; b>>=1) {
		if (b&1) acc ^= a;
		a = (a<<1) ^ (a&0x80 ? 0xc3 : 0x0);
		// printf("[%02x %02x %02x]  ", a,b,acc);
	}
	return acc;
}

void mixcolumns_155(unsigned char state[16]) {
	short j;          // Y+1..2
	short i;          // Y+3..4
	unsigned char c;  // Y+5
	//char** state;   // Y+6..7
	for (i = 0; i<16; i++) {
		c = state[0+15];
		for (j = 14; j>=0; j--) {
			state[j+1] = state[j];
			c ^= sub_129(state[j], byte_102210[j]);
		}
		state[0] = c;
		// printf("intermediate mixing:  "); print_data(state, 16);
	}
	return;
}

